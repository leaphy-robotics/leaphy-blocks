function getCodeGenerators(arduino) {
  arduino.forBlock["procedures_defreturn"] = function(block) {
    const funcName = arduino.nameDB_?.getName(
      block.getFieldValue("NAME"),
      Blockly.Names.NameType.PROCEDURE
    );
    let branch = arduino.statementToCode(block, "STACK");
    if (arduino.STATEMENT_PREFIX) {
      branch = arduino.prefixLines(
        arduino.STATEMENT_PREFIX.replace(
          /%1/g,
          "'" + block.id + "'"
        ),
        arduino.INDENT
      ) + branch;
    }
    if (arduino.INFINITE_LOOP_TRAP) {
      branch = arduino.INFINITE_LOOP_TRAP.replace(
        /%1/g,
        "'" + block.id + "'"
      ) + branch;
    }
    let returnValue = arduino.valueToCode(block, "RETURN", arduino.ORDER_NONE) || "";
    if (returnValue) {
      returnValue = "  return " + returnValue + ";\n";
    }
    const args = [];
    const { arguments_ } = block;
    for (let x = 0; x < arguments_.length; x++) {
      args[x] = "double " + arduino.nameDB_?.getName(
        arguments_[x],
        Blockly.Names.NameType.VARIABLE
      );
    }
    let returnType;
    if (block.type == "procedures_defreturn") {
      const checks = block.getInput("RETURN")?.connection?.targetConnection?.getCheck();
      if (checks && checks[0]) returnType = arduino.TYPES[checks[0]];
      else returnType = "double";
    } else {
      returnType = "void";
    }
    let code = `${returnType} ${funcName}(${args.join(", ")}) {
` + branch + returnValue + "}";
    code = arduino.scrub_(block, code);
    arduino.addDeclaration(funcName, code, true);
    return null;
  };
  arduino.forBlock["procedures_defnoreturn"] = arduino.forBlock["procedures_defreturn"];
  arduino.forBlock["procedures_callreturn"] = function(block) {
    const funcName = arduino.nameDB_?.getName(
      block.getFieldValue("NAME"),
      Blockly.Names.NameType.PROCEDURE
    );
    const args = [];
    const { arguments_ } = block;
    for (let x = 0; x < arguments_.length; x++) {
      args[x] = arduino.valueToCode(block, "ARG" + x, arduino.ORDER_NONE) || "null";
    }
    const code = funcName + "(" + args.join(", ") + ")";
    return [code, arduino.ORDER_UNARY_POSTFIX];
  };
  arduino.forBlock["procedures_callnoreturn"] = function(block) {
    const funcName = arduino.nameDB_?.getName(
      block.getFieldValue("NAME"),
      Blockly.Names.NameType.PROCEDURE
    );
    const args = [];
    const { arguments_ } = block;
    for (let x = 0; x < arguments_.length; x++) {
      args[x] = arduino.valueToCode(block, "ARG" + x, arduino.ORDER_NONE) || "null";
    }
    return funcName + "(" + args.join(", ") + ");\n";
  };
  arduino.forBlock["procedures_ifreturn"] = function(block) {
    const condition = arduino.valueToCode(block, "CONDITION", arduino.ORDER_NONE) || "false";
    let code = "if (" + condition + ") {\n";
    const { hasReturnValue_ } = block;
    if (hasReturnValue_) {
      const value = arduino.valueToCode(block, "VALUE", arduino.ORDER_NONE) || "null";
      code += "  return " + value + ";\n";
    } else {
      code += "  return;\n";
    }
    code += "}\n";
    return code;
  };
  arduino.forBlock["arduino_functions"] = function(block) {
    function statementToCodeNoTab(block2, name) {
      const targetBlock = block2.getInputTargetBlock(name);
      return arduino.blockToCode(targetBlock);
    }
    const setupBranch = arduino.statementToCode(block, "SETUP_FUNC");
    if (setupBranch) {
      arduino.addSetup("userSetupCode", setupBranch, true);
    }
    return statementToCodeNoTab(block, "LOOP_FUNC");
  };
}
